/**
 * @param {string} category — 'all' или id категории
 * @param {string} search — подстрока по question (без регистра)
 */
export function filterAnalyticsQuestions(category, search) {
  const q = String(search || '').trim().toLowerCase();
  return analyticsQuestions.filter((item) => {
    if (category && category !== 'all' && item.category !== category) return false;
    if (!q) return true;
    return item.question.toLowerCase().includes(q);
  });
}
