/**
 * @param {string[]} ids
 * @returns {string}
 */
export function getMergedSourceTooltipText(ids) {
  if (!Array.isArray(ids) || !ids.length) return '';
  const parts = [];
  for (const rawId of ids) {
    const id = String(rawId || '').trim();
    const chunk = ADVICE_SOURCE_CHUNKS[id];
    if (!chunk) continue;
    const header = chunk.title ? `«${chunk.title}»\n\n` : '';
    parts.push(header + String(chunk.body || '').trim());
  }
  return parts.join('\n\n────────\n\n');
}
